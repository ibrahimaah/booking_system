     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            'name' => 'required'
        ]);

        $name = $request->name;
        $floor = new Floor();
        $floor->name = $name;

        if($floor->save())
        {
            return back()->with('success','A New Floor added successfuly :)');
        }
        else
        {
            return back()->with('faild','Cann\'t add a new Floor :(');
        } 
    }

